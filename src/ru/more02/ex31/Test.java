package ru.more02.ex31;

public class Test {
    int a;
    int b;
    int result;

    public Test(int a, int b, int result) {
        this.a = a;
        this.b = b;
        this.result = result;
    }


    public int summ(int a, int b, int result) {
        return result = a+b;
    }
    public int multiplication (int a, int b, int result) {
        return result = a*b;
    }
    public int divide (int a, int b, int result) {
        return result = a/b;
    }
}
